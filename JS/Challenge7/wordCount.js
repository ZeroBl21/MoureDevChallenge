const wordCount = (phrase) => {
    const words = phrase.toLowerCase().split(" ");
    let obj = {};

    for (const word of words) {
        obj[word] = isNaN(obj[word]) ? 1 : obj[word] + 1;
    }

    const sortable = Object.fromEntries(
        Object.entries(obj).sort(([, a], [, b]) => a - b)
    );

    return sortable;
};

